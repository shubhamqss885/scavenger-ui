export type IconName = keyof typeof iconMap;

export type IconSize = "xxs" | "xs" | "sm" | "md" | "lg" | "xl";

export type IconVariant =
  | "default"
  | "foreground"
  | "primary"
  | "secondary"
  | "muted"
  | "destructive"
  | "success"
  | "warning"
  | "white";

interface IconProps {
  name: IconName;
  size?: IconSize;
  variant?: IconVariant;
  className?: string;
  strokeWidth?: number;
  fill?: string;
}

const sizeMap: Record<IconSize, string> = {
  xxs: "h-3 w-3 min-w-3",
  xs: "h-3.5 w-3.5 min-w-3.5",
  sm: "h-4 w-4 min-w-4",
  md: "h-5 w-5 min-w-5",
  lg: "h-6 w-6 min-w-6",
  xl: "h-8 w-8 min-w-8",
};

const variantMap: Record<IconVariant, string> = {
  default: "text-muted-foreground", // Changed to match most text in the app (slate-700)
  foreground: "text-foreground", // For darker text (slate-900 in light mode)
  primary: "text-primary",
  secondary: "text-secondary",
  muted: "text-slate-400", // Lighter muted color (slate-400)
  destructive: "text-destructive",
  success: "text-green-700 dark:text-green-500",
  warning: "text-yellow-700 dark:text-yellow-500",
  white: "text-white",
};

export const Icon: React.FC<IconProps> = ({
  name,
  size = "md",
  variant = "default",
  className,
  strokeWidth = 2,
  fill = "none",
}) => {
  const IconComponent = iconMap[name];

  if (!IconComponent) {
    console.warn(
      `Icon "${name}" not found in icon map. Add it to the iconMap in icon.tsx`,
    );
    return null;
  }

  return (
    <IconComponent
      className={cn(sizeMap[size], variantMap[variant], className)}
      strokeWidth={strokeWidth}
      fill={fill}
    />
  );
};
